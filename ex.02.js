module.exports = function Reverse(str=''){
    let char = [];
    let newChar = [];
    
    for(let i = 0 ; i< str.length ; i ++){
        char[i] = str[i];
    }
    
        switch(char){
            case char[0]:
                newChar[3];
                break;
            case char[2]:
                newChar[1];
                break;
            case char[1]:
                newChar[2];
                break;
            case char[3]:
                newChar[0];
                break;
            
        }
    
    

    return newChar
    
}